    private String mahoa1(String banro, String key){
        int n = banro.length();
        String banma = "";
        int k = 0;
        for(int i = 0; i < n; i++){
            if(Character.isLetter(banro.charAt(i))){
                banma += mahoa2(banro.charAt(i), key.charAt(k));
                k++;
                k = k%key.length();
            }else{
                banma += banro.charAt(i);
            }      
        }
         return banma;
    }
    char mahoa2(char x, char k){
        int xn = Character.toUpperCase(x)-'A';
        int kn = Character.toUpperCase(k)-'A';
        int yn = vig[kn][xn];
        return (char)(yn+'A');
    }

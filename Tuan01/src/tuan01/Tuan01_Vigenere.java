    private void btnGiaMaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGiaMaActionPerformed
        // TODO add your handling code here:
        String banma = this.txtBanMa.getText();
        String k = this.txtKhoa.getText();
        String kt1="";
        int kn = k.length();
        for(int i = 0; i<kn; i++){
            kt1 += (char)(((26-(Character.toUpperCase(k.charAt(i))-'A'))%26)+'A');
        }
        this.txtKhoa.setText(kt1);
        String banro = mahoa1(banma,kt1);
        this.txtBanRo.setText(banro);
    }//GEN-LAST:event_btnGiaMaActionPerformed

